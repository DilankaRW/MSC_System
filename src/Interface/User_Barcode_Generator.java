    private void DateandTime() {
        Timer t = new Timer(100, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                Date d = new Date();
                SimpleDateFormat sdf_Time = new SimpleDateFormat("hh:mm:ss aa");
                SimpleDateFormat sdf_Date = new SimpleDateFormat("YYYY-MM-dd");
                String time = sdf_Time.format(d);
                String Date = sdf_Date.format(d);
                lbl_Time.setText(time);
                lbl_Date.setText(Date);
            }
        });
        t.start();
    }
    
    private void Barcode_ID() {
        try {
            ResultSet rs = new Database_Connection.DB().getData("SELECT MAX(barcode_id) AS LargestID FROM barcode;");
            if (rs.next()) {
                String id = rs.getString("LargestID");
                lbl_barcode_id.setText(Integer.parseInt(id) + 1 + "");
            }
        } catch (Exception e) {
            System.out.println(e);
            lbl_barcode_id.setText("1");
        }
    }
    
    private void Print_Barcode() {
        try {
            String reportSourse = "src\\report\\Barcode.jrxml";
            HashMap<String, Object> params = new HashMap<String, Object>();
            params.put("Barcode", Integer.parseInt(lbl_barcode_id.getText()));
            JasperReport jasperReport = JasperCompileManager.compileReport(reportSourse);
            Connection connseacrch = new Database_Connection.DB().myCon();
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, params, connseacrch);
            JasperViewer.viewReport(jasperPrint, false);
            connseacrch.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
