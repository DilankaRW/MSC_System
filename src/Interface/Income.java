    private void Invoice_Income_Table_Loader() {
        DefaultTableModel dtm = (DefaultTableModel) jtbl_Invoice_income.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet set = new DB().getData("SELECT * FROM invoice I INNER JOIN customer C ON I.customer_id = C.customer_id INNER JOIN invoice_payment IP ON I.invoice_id=IP.invoice_invoice_id ");

            while (set.next()) {
                Vector v = new Vector();
                v.add(set.getString("I.invoice_id"));
                v.add(set.getString("I.date"));
                v.add(set.getString("I.customer_id"));
                v.add(set.getString("C.name"));
                v.add(set.getString("I.customer_credit"));
                v.add(set.getString("I.net_total"));
                dtm.addRow(v);

                Invoice_Income_Table_Total();
            }
        } catch (Exception e) {
        }
    }

    private void Date_Search() {
        DefaultTableModel dtm = (DefaultTableModel) jtbl_Invoice_income.getModel();

        try {
            dtm.setRowCount(0);
            ResultSet rs = new DB().getData("select * from invoice INNER JOIN customer ON customer.customer_id = invoice.customer_id where invoice.date between '" + jtxt_Invoice_income_search.getText() + "' and '" + jtxt_Invoice_income_search1.getText() + "' && invoice.release='yes' && invoice.active='yes'");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("invoice.invoice_id"));
                v.add(rs.getString("invoice.date"));
                v.add(rs.getString("invoice.customer_id"));
                v.add(rs.getString("customer.name"));
                v.add(rs.getString("invoice.discount"));
                v.add(rs.getString("invoice.customer_credit"));
                v.add(rs.getString("invoice.net_total"));
                dtm.addRow(v);
                Invoice_Income_Table_Total();
                Invoice_Income_Table_Credit();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

