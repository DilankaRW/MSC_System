    private void jbtn_Supplier_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_Supplier_SaveActionPerformed
        if (!jtxt_Supplier_ID.getText().isEmpty() && !jtxt_Supplier_Name.getText().isEmpty()) {
            try {
                int Status = 0;

                if (jcmb_Supplier_Status.getSelectedItem().toString().equals("Active")) {
                    Status += 1;
                }
                if (jcmb_Supplier_Status.getSelectedItem().toString().equals("Deactive")) {
                    Status += 0;
                }

                new Database_Connection.DB().putData("insert into supplier (supplier_id, code, name,company_name, no, street, city, contact_no, email, description, status) values('" + jtxt_Supplier_ID.getText() + "','" + jtxt_Supplier_Code.getText() + "','" + jtxt_Supplier_Name.getText() + "','" + jtxt_Supplier_Company_Name.getText() + "','" + jtxt_Supplier_Address_No.getText() + "','" + jtxt_Supplier_Address_Street.getText() + "','" + jtxt_Supplier_Address_City.getText() + "' ,'" + jtxt_Supplier_Contact_Number.getText() + "','" + jtxt_Supplier_Email.getText() + "','" + jtxt_Supplier_Description.getText() + "','" + Status + "')");
                JOptionPane.showMessageDialog(this, "Supplier Registered Successfully", "Successfully Message", JOptionPane.INFORMATION_MESSAGE);

                supplier_text_clean();
                Supplier_Table_Loader_Data();

            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Check All Input Field", "Information Message", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jbtn_Supplier_SaveActionPerformed

