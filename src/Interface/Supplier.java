    private void jtbl_SupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbl_SupplierMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) jtbl_Supplier.getModel();
        int Selected_Row = jtbl_Supplier.getSelectedRow();
        jtxt_Supplier_ID.setText(dtm.getValueAt(Selected_Row, 0).toString());
        try {
            ResultSet rs = new Database_Connection.DB().getData("SELECT * FROM supplier WHERE supplier_id = '" + jtbl_Supplier.getValueAt(jtbl_Supplier.getSelectedRow(), 0) + "'");
            while (rs.next()) {
                jtxt_Supplier_Code.setText(rs.getString("code"));
                jtxt_Supplier_Name.setText(rs.getString("name"));
                jtxt_Supplier_Company_Name.setText(rs.getString("company_name"));
                jtxt_Supplier_Address_No.setText(rs.getString("no"));
                jtxt_Supplier_Address_Street.setText(rs.getString("street"));
                jtxt_Supplier_Address_City.setText(rs.getString("city"));
                jtxt_Supplier_Contact_Number.setText(rs.getString("contact_no"));
                jtxt_Supplier_Email.setText(rs.getString("email"));
                jtxt_Supplier_Description.setText(rs.getString("description"));
                if (rs.getString("status").equals("1")) {
                    jcmb_Supplier_Status.setSelectedItem("Active");
                } else {
                    jcmb_Supplier_Status.setSelectedItem("Deactive");
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jtbl_SupplierMouseClicked

