    private void GRN_Stock_Update() {
        DefaultTableModel dtm = (DefaultTableModel) jTbl_Inv_Item.getModel();
        if (jTbl_Inv_Item.getRowCount() != 0) {

            for (int i = 0; i < jTbl_Inv_Item.getRowCount(); i++) {
                try {
                    String Pro_ID = dtm.getValueAt(i, 0).toString();
                    String S_ID = dtm.getValueAt(i, 3).toString();

                    new DB().putData("INSERT INTO customer_return_item (invoice_id, item_id, grn_id, price, qty, total) VALUES('" + jlbl_Customer_Return_Inv_No.getText() + "','" + Pro_ID + "','" + Integer.parseInt(dtm.getValueAt(i, 2).toString()) + "','" + Double.parseDouble(dtm.getValueAt(i, 4).toString()) + "','" + Double.parseDouble(dtm.getValueAt(i, 8).toString()) + "','" + Double.parseDouble(dtm.getValueAt(i, 9).toString()) + "')");

                    String GR_ID = dtm.getValueAt(i, 2).toString();

                    ResultSet rs = new Database_Connection.DB().getData("SELECT * FROM grn G INNER JOIN grn_item GI ON G.grn_id = GI.grn_id INNER JOIN item I ON GI.item_id = I.item_id  INNER JOIN supplier S ON G.supplier_id = S.supplier_id INNER JOIN grn_stock GS ON G.grn_id=GS.grn_id WHERE G.grn_id = '" + Integer.parseInt(GR_ID) + "' and GI.item_id='" + Integer.parseInt(Pro_ID) + "' ");
                    while (rs.next()) {
                        int Table_PQTY = Integer.parseInt(dtm.getValueAt(i, 8).toString());
                        int Database_PQTY = Integer.parseInt(rs.getString("GS.stock_qty").toString());
                        int New_PQTY = Database_PQTY + Table_PQTY;

                        new Database_Connection.DB().putData("Update grn_stock Set stock_qty = '" + New_PQTY + "' Where grn_id = '" + GR_ID + "' and item_id = '" + Pro_ID + "'");
                    }

                } catch (Exception e) {
                    System.out.println("Error 3 ..... ");
                    e.printStackTrace();
                }
            }
            JOptionPane.showMessageDialog(this, "Invoice Saved Successfully", "Successfully Message", JOptionPane.INFORMATION_MESSAGE);
            dtm.setRowCount(0);

        } else {
            JOptionPane.showMessageDialog(this, "No Product add in Receipt", "Infromation Message", JOptionPane.INFORMATION_MESSAGE);
        }
    }

